/// Fixtures the chumsky oracle DECLINES (parse error / recovery) within the S2
/// subset — a guard that the handwritten parser is never MORE permissive than the
/// oracle (accepting something the grammar rejects). Each must decline through BOTH
/// frontends when wrapped as `let __probe = <fixture>;`.
const DECLINER_FIXTURES: &[&str] = &[
    "(a, b,)",   // the tuple atom has no `allow_trailing` (unlike a list)
    "(a,)",      // one element + trailing comma is neither tuple nor group
    "a < < b",   // a non-adjacent `< <` is not a shift (and not a valid comparison)
    "x is",      // `is` with no pattern
    "|x|",       // a closure with no body
    "f(a b)",    // a missing argument comma
    "[a; b; c]", // a repeat is `expr ; expr`, not two `;`
    "a::",       // a `::` with no member
    "match x {", // an unclosed match
    "(",         // an unclosed paren
];
